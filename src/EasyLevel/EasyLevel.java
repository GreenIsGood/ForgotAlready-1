    public static ArrayList<Integer> Create1(int n){
        ArrayList<Integer> A  = new ArrayList<>();
        int k=1;
        for(int i=0;i<n*2;i++){
            if(i%2==0&&i!=0)
                k++;
            A.add(k);
        }
        
        ArrayList<Integer> b = new ArrayList<>();
        for(int i=0;i<2*n;i++){
            int o=A.size()-1;
            int a=(int) (Math.random()*o);
            b.add(A.get(a));
            A.remove(a);
        }
        return b;
    }

