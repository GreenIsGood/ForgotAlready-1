    private void Button1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button1ActionPerformed
        if(whatOpen != Button1){
            Button1.setIcon(new ImageIcon(photos[x[0]]));
            frame.update(getGraphics());
            delay(500);
            if(isOpen){
                if(noOpen == x[0]){
                    Home.audioClip3.loop(1);
                    Home.audioClip3.start();
                    whatOpen.setVisible(false);
                    Button1.setVisible(false);
                    count += 2;
                    if(count == 8){
                        Home.audioClip2.loop(1);
                        Home.audioClip2.start();
                        Winning.from = "Level1";
                        Winning.score.setText(String.valueOf(score));
                        Winning.main(null);
                        frame.setVisible(false);
                        if((score) > scoreFile){
                            try {
                                output = new PrintWriter(Home.scoreEasy);
                            } catch (FileNotFoundException ex) {
                                Logger.getLogger(EasyLevel.class.getName()).log(Level.SEVERE, null, ex);
                            }
                            output.print(score);
                            output.close();
                        }
                    }
                }
                else{
                    whatOpen.setIcon(new ImageIcon(hidden));
                    Button1.setIcon(new ImageIcon(hidden));
                }
                whatOpen = null;
                isOpen = false;
                noOpen = -1;
            }
            else{
                isOpen = true;
                noOpen = x[0];
                whatOpen = Button1;
            }
        }
    }//GEN-LAST:event_Button1ActionPerformed

